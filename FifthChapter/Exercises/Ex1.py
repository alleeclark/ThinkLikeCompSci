def dayname(dayNumber):
    if dayNumber == 0:
        return "Sunday"
    elif dayNumber == 1:
        return "Monday"
    elif dayNumber == 2:
        return "Tuesday"
    elif dayNumber == 3:
        return "Wednesday"
    elif dayNumber == 4:
        return "Thursday"
    elif dayNumber == 5:
        return "Friday"
    elif dayNumber == 6:
        return "Saturday"